public class DaireHesap {
    public static void main (String [] args) {
        double pi = 3.14159;
        double r = 5.5;
        double alan = pi * r * r;
        double cevre = 2 * pi * r;
        System.out.println("Yaricap: " + r);
        System.out.println("Alan: " + alan);
        System.out.println("Cevre: " + cevre);
    }
}
